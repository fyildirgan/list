# List Methods
# Eğer dir(notes) dersek listede bulunan metodları görebiliriz.
notes = [1, 2, 3, 4]
print(dir(notes))
# len: builtin pyhton fonksiyonu, boyut bilgisi.
print(len(notes))
# append: eleman ekler.
notes.append(100)
print(notes)
# pop: indexe göre siler.
notes.pop(0)
print(notes)
# insert: indexe ekler.
notes.insert(2, 99)
print(notes)
